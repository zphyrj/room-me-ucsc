  <h1>Housing FAQ</h1>
  <h2>What's the difference between the apartments and the dorms?</h2>
  <p>Apartments are only allowed for non-frst years to apply to. Different apartments at each college have different capacities. For example, some 
    apartments can have six people whereas another can have four people, every building is different. Apartments, in addition to bedrooms, have a bathroom,
    a kitchen, and a living room. If you are in an apartment, you do not need to opt-in for a meal plan. Each apartment may have different rooms such
    as singles, doubles, and triples. 

    Dorms are typically allowed for first years with non-first years on certain floors. Dorms have communal bathrooms, with no kitchen or living room. It is just a room
    that is either a single, double, or triple in a building with no other attachments. Students in dorms must have a meal plan. 
  </p>
  <h2>Can continuing students be in a dorm?</h2>
  <p>Continuing students can apply to live in dorms, however, they will be put into a floor with other continuing students instead of a floor with all first years.</p>
  <h2>How can I request a roommate?</h2>
  <p>Once you find a roommate, when it is your housing appointment time, be sure that all parties have applied to the same college and choose the corresponding
    rooms for your group or roommate. Whoever has the first appointment time is responsible for choosing house for everyone. Make sure to have a backup plan as 
    housing will fill up quickly.
  </p>
  <h2>How are enrollment dates determined?</h2>
  <p>Unfortunately by a lottery.</p>
  <h2>What's the layout of apartments?</h2>
  <p>Each apartment is different depending on the college it is located at. All apartments have bedrooms, bathrooms, a living room, and a kitchen. Check the 
    UCSC's housing website to view layouts and items in certain apartments. Apartments will have a mix of different bedrooms such as two singles and two doubles or 
    two triples. 
  </p>
  <h2>What are the different layouts between Colleges?</h2>
  <p>Each college has their own theme and different locations. Looking on the UCSC's campus map will offer more information as to where each college is located.
    Each college may have different buildings with different capacities such as six person apartments, four person apartments, etc.
  </p>
  <h2>What final tips do you have?</h2>
  <p>Don't forget to check your email for housing appointment times. Research prices for different rooms and consider alterantive housing options. Don't be afraid to reach out to 
    other students in order to get to know them. Ask yourself, would you rather be stuck with a roommate who you dislike or live with a roommate you like because you reached out and asked questions?
  </p>
</div>

